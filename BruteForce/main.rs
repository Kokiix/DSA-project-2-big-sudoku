fn count_clues(board: &Vec<Vec<u8>>) -> usize {
    board.iter().flatten().filter(|&&cell| cell != 0).count()
}

fn print_board(board: &Vec<Vec<u8>>) {
    for (i, row) in board.iter().enumerate() {
        if i % 3 == 0 && i != 0 {
            println!("------+-------+------");
        }
        for (j, &cell) in row.iter().enumerate() {
            if j % 3 == 0 && j != 0 {
                print!("| ");
            }
            if cell == 0 {
                print!(". ");
            } else {
                print!("{} ", cell);
            }
        }
        println!();
    }
}
