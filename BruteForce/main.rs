#[wasm_bindgen]
pub fn solve(n: usize, board_1d: Vec<u8>) -> Vec<u8> {
    let board_2d = board_1d
        .chunks(n)
        .map(|x| Vec::from(x))
        .collect::<Vec<Vec<u8>>>();

    let mut puzzle = Sudoku::new(n);
    puzzle.board = board_2d;
    let mut solver = Solver::new(puzzle);
    solver.solve();
    return solver.board.into_iter().flatten().collect();
}

